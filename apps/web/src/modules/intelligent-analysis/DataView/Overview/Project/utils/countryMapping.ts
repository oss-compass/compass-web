/**
 * 根据当前语言环境决定是否需要翻译
 * @param text 原始文本
 * @param mapping 映射表
 * @param locale 当前语言环境
 * @returns 翻译后的文本或原文本
 */
export const translateByLocale = (
  text: string,
  mapping: Record<string, string>,
  locale: string
): string => {
  if (locale === 'en') {
    // 首先尝试使用映射表翻译
    const mappedText = mapping[text] || text;

    // 如果是英文环境，去掉常见的中文字符
    return mappedText.replace(/生态/g, '').replace(/主仓/g, '').trim();
  }
  return text;
};

// 处理复合字符串的翻译函数，如 '核心/核心/核心/核心' -> 'Core/Core/Core/Core'
export const translateCompositeString = (
  text: string,
  mapping: Record<string, string>,
  locale: string,
  separator: string = '/'
): string => {
  if (locale === 'en') {
    // 按分隔符分割字符串
    const parts = text.split(separator);
    // 逐个翻译每个部分
    const translatedParts = parts.map((part) => {
      const trimmedPart = part.trim();
      return mapping[trimmedPart] || trimmedPart;
    });
    // 重新组合
    return translatedParts.join(separator);
  }
  return text;
};
